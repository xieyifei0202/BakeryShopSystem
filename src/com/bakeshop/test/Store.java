package com.bakeshop.test;

public class Store {
    private int storeId;
    private String storeAddress;
    private String storeCity;
    private String storeSuburb;
    private int storePostal;
    private int storeNum;

    public int getStoreId() {
        return storeId;
    }

    public String getStoreAddress() {
        return storeAddress;
    }

    public String getStoreCity() {
        return storeCity;
    }

    public String getStoreSuburb() {
        return storeSuburb;
    }

    public int getStorePostal() {
        return storePostal;
    }

    public int getStoreNum() {
        return storeNum;
    }

    public void setStoreId(int storeId) {
        this.storeId = storeId;
    }

    public void setStoreAddress(String storeAddress) {
        this.storeAddress = storeAddress;
    }

    public void setStoreCity(String storeCity) {
        this.storeCity = storeCity;
    }

    public void setStoreSuburb(String storeSuburb) {
        this.storeSuburb = storeSuburb;
    }

    public void setStorePostal(int storePostal) {
        this.storePostal = storePostal;
    }

    public void setStoreNum(int storeNum) {
        this.storeNum = storeNum;
    }
}
